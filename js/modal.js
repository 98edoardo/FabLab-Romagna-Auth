$(document).ready(function () {

    var modals = $('div.modal')

    modals.each(function () {
        modals.find('button[type=\'reset\']').each(function () {
            $(this).click(function () {
                console.log('ciao')
                if (modals.hasClass('is-active'))
                    modals.removeClass('is-active')
            })

        })

        modals.children('div.modal-background').each(function () {
            $(this).click(function () {
                modals.find('button[type=\'reset\']').each(function (i) {
                    if (i !== 0)
                        return

                    $(this).click()
                })
            })
        })
    })

    var openModals = $('.open-modal').each(function () {
        $(this).click(function () {

            $('#' + this.dataset.open).each(function () {
                $(this).addClass('is-active')
            })
        })

    })

    bulmaCalendar.attach('.calendar-input', {
        displayMode: 'dialog',
        dateFormat: 'DD/MM/YYYY',
        lang: 'it',
        weekStart: 1,
        showHeader: false
    })
})
